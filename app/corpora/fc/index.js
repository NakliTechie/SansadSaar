// Three LS-chaired committees: Public Accounts (PAC), Estimates,
// Public Undertakings (COPU). Daily-mirrored from sansad.in via
// parliamentwatch-data's build_fc.py + fc.yml.
//
// Fifth registered corpus alongside DRSC, CAG, Bills, LC. Uses the
// composite reportKey shape `fc|<committee>|<lok_sabha>|<report_number>`
// (similar to DRSC's `drsc|<committee>|<ls>|<num>`) — same report
// number can recur across LS terms and across committees, so each
// dimension is part of the key.
