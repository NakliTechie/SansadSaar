// upstream we touch. DRSC and Bills sit on the same host with no
// archive; FC follows that pattern.
//
// Independence Principle: this file does not import from cag, lc,
// drsc, or bills corpus modules. Some helper code is duplicated
// (date parsing, filter handlers); intentional per spec v0.4.

