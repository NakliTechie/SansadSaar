function _csvCell(v) {
  if (v == null) return '';
  const s = String(v);
  if (/[",\n\r]/.test(s)) return '"' + s.replace(/"/g, '""') + '"';
  return s;
}

function _downloadBlob(filename, mime, content) {
  const blob = new Blob([content], { type: mime });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url; a.download = filename;
  document.body.appendChild(a); a.click();
  setTimeout(() => { URL.revokeObjectURL(url); a.remove(); }, 100);
}

function _ts() {
  const d = new Date();
  const pad = n => String(n).padStart(2, '0');
  return `${d.getFullYear()}${pad(d.getMonth()+1)}${pad(d.getDate())}-${pad(d.getHours())}${pad(d.getMinutes())}`;
}

function exportMetadataCSV() {
  const rows = state.filtered;
  if (!rows.length) { _deps.ui.toast('No reports to export'); return; }
  const headers = ['committee', 'lok_sabha', 'report_number', 'title',
    'presented_in_ls', 'laid_in_rs', 'date_of_adoption',
    'pdf_url', 'has_text', 'has_summary'];
  const lines = [headers.join(',')];
  for (const r of rows) {
    lines.push([
      r.committee, r.lok_sabha, r.report_number, r.title,
      r.presented_in_ls, r.laid_in_rs, r.date_of_adoption,
      r.pdf_url,
      hasExtractedText(r) ? 'yes' : 'no',
      hasSummary(r) ? 'yes' : 'no',
    ].map(_csvCell).join(','));
  }
  _downloadBlob(`sansadsaar-fc-metadata-${_ts()}.csv`, 'text/csv', lines.join('\n') + '\n');
  _deps.ui.toast(`Exported ${rows.length} Financial Committee reports`);
}

function exportSummariesMD() {
  const all = getAllReports();
  const out = [];
  out.push(`# SansadSaar — Financial Committee summaries`);
  out.push('');
  out.push(`Exported ${new Date().toLocaleString('en-IN')}. Source: <https://sansadsaar.naklitechie.com>.`);
  out.push('');
  let n = 0;
  for (const r of all) {
    const key = reportKey(r);
    const summary = state.cache.summaries[key];
    if (!summary) continue;
    n++;
    out.push('---');
    out.push('');
    const cmt = COMMITTEES[r.committee] || { long: r.committee };
    out.push(`## ${cmt.long} #${r.report_number} (LS${r.lok_sabha}) — ${r.title || '(untitled)'}`);
    out.push('');
    const meta = [r.presented_in_ls, r.laid_in_rs, r.date_of_adoption].filter(Boolean).join(' · ');
    if (meta) out.push(`*${meta}*`);
    if (r.pdf_url) out.push(`*PDF: <${r.pdf_url}>*`);
    out.push('');
    out.push(summary);
    out.push('');
  }
  if (!n) { _deps.ui.toast('No FC summaries cached yet'); return; }
  _downloadBlob(`sansadsaar-fc-summaries-${_ts()}.md`, 'text/markdown', out.join('\n'));
  _deps.ui.toast(`Exported ${n} summaries`);
}

