function populateFilters() {
  const reports = getAllReports();
  const lsSet   = new Set();
  const yearSet = new Set();
  for (const r of reports) {
    if (r.lok_sabha != null) lsSet.add(r.lok_sabha);
    if (r._year) yearSet.add(r._year);
  }

  const cmtSel = document.getElementById('filterCommittee');
  if (cmtSel) {
    cmtSel.innerHTML = '<option value="">All committees</option>' +
      Object.entries(COMMITTEES).map(([k, c]) =>
        `<option value="${k}">${escapeHtml(c.long)}</option>`).join('');
  }
  const lsSel = document.getElementById('filterLokSabha');
  if (lsSel) {
    lsSel.innerHTML = '<option value="">All LS terms</option>' +
      [...lsSet].sort((a, b) => b - a).map(v =>
        `<option value="${v}">${v}th Lok Sabha</option>`).join('');
  }
  const yearSel = document.getElementById('filterYear');
  if (yearSel) {
    yearSel.innerHTML = '<option value="">All years</option>' +
      [...yearSet].sort((a, b) => b - a).map(v =>
        `<option value="${v}">${v}</option>`).join('');
  }

  if (cmtSel)  cmtSel.value  = state.filters.committee || '';
  if (lsSel)   lsSel.value   = state.filters.lok_sabha || '';
  if (yearSel) yearSel.value = state.filters.year      || '';
}

