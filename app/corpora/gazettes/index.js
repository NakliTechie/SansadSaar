// ── Helpers ─────────────────────────────────────────────────────────────────

function reportKey(r) {
  return `gazettes|central|${r.type_letter}|${r.issue_date}|${r.ugid}`;
}

// Composite id used in the text shards' record_to_shard map. Mirrors
// build_gazettes.py's file_id() format: `central_<e|w>_<YYYYMMDD>_<ugid>`.
function fileId(r) {
  const date_compact = (r.issue_date || '').replace(/-/g, '');
  return `central_${r.type_letter}_${date_compact}_${r.ugid}`;
}

function reportDate(r) {
  // Gazette issue_date is always ISO YYYY-MM-DD from build_gazettes.py.
  if (!r.issue_date) return null;
  const d = new Date(r.issue_date);
  return isNaN(d) ? null : d;
}

function reportYear(r) {
  if (r.issue_date) {
    const y = parseInt(r.issue_date.slice(0, 4), 10);
    if (!isNaN(y)) return y;
  }
  return null;
}

function formatDate(d) {
  if (!d) return '—';
  return d.toLocaleDateString('en-IN', { day: '2-digit', month: 'short', year: 'numeric' });
}

function categoryLabel(r) {
  if (r.category === 'extraordinary') return 'Extraordinary';
  if (r.category === 'weekly') return 'Weekly';
  return r.category || '?';
}

function languageKey(r) {
  const langs = r.languages || [];
  if (langs.length === 0) return '?';
  if (langs.includes('eng') && langs.includes('hin')) return 'eng+hin';
  return langs[0];
}

function langInfo(r) {
  return LANG_INFO[languageKey(r)] || DEFAULT_LANG;
}

// ── Data fetching ───────────────────────────────────────────────────────────

// Sharded reports loader. The gazettes index emits reports-meta.json
// (manifest) + reports-central-NN.json (records). Returns a flat array.
async function fetchReports(dataUrl, v, fetchOpts) {
  const metaResp = await fetch(dataUrl + CORPUS_PREFIX + 'reports-meta.json' + v, fetchOpts);
  if (!metaResp.ok) throw new Error(`reports-meta.json: ${metaResp.status}`);
  const reportsMeta = await metaResp.json();
  const shards = (reportsMeta?.shards?.central) || [];
  const tasks = shards.map(entry =>
    fetch(dataUrl + CORPUS_PREFIX + entry.file + v, fetchOpts)
      .then(r => {
        if (!r.ok) throw new Error(`${entry.file}: ${r.status}`);
        return r.json();
      })
      .then(payload => ({ idx: payload?.shard_index ?? 0, records: payload?.records || [] }))
  );
  const shardResults = await Promise.all(tasks);
  shardResults.sort((a, b) => a.idx - b.idx);
  const merged = [];
  for (const { records } of shardResults) merged.push(...records);
  return merged;
}

async function fetchData(forceRefresh = false) {
  const splash    = document.getElementById('splash');   // shared with shell
  const splashErr = document.getElementById('splashError');
  const dataUrl   = _deps.config.dataBaseUrl;

  if (!forceRefresh) {
    try {
      const [reportsBlob, manifestBlob, metaBlob] = await Promise.all([
        idbGet('blobs', 'gazettes-reports.json'),
        idbGet('blobs', 'gazettes-manifest.json'),
        idbGet('blobs', 'gazettes-meta.json'),
      ]);
      if (reportsBlob && manifestBlob) {
        state.data.reports  = reportsBlob;
        state.data.manifest = manifestBlob;
        state.data.meta     = metaBlob || null;
      }
    } catch (e) { console.warn('gazettes IDB cache miss', e); }
  }

  const haveCache = Array.isArray(state.data.reports) && state.data.reports.length > 0;
  const bucket = Math.floor(Date.now() / 300000);
  const v = forceRefresh ? `?v=${Date.now()}` : `?v=${bucket}`;
  const fetchOpts = { cache: 'no-cache' };

  try {
    const [reports, manifest, meta] = await Promise.all([
      fetchReports(dataUrl, v, fetchOpts),
      fetch(dataUrl + CORPUS_PREFIX + 'manifest.json' + v, fetchOpts).then(r => r.ok ? r.json() : { texts: {} }).catch(() => ({ texts: {} })),
      fetch(dataUrl + CORPUS_PREFIX + 'meta.json'     + v, fetchOpts).then(r => r.ok ? r.json() : null).catch(() => null),
    ]);
    state.data.reports  = reports;
    state.data.manifest = manifest;
    state.data.meta     = meta;

    idbPut('blobs', 'gazettes-reports.json',  reports).catch(() => {});
    idbPut('blobs', 'gazettes-manifest.json', manifest).catch(() => {});
    if (meta) idbPut('blobs', 'gazettes-meta.json', meta).catch(() => {});
  } catch (err) {
    console.error('gazettes fetch failed', err);
    if (!haveCache) {
      _deps.ui.toast('Could not fetch Gazettes data — see console for details');
      return false;
    }
    _deps.ui.toast('Gazettes offline — using cached data');
  }

  splash?.classList.add('hide');
  return true;
}

async function fetchReportText(report) {
  const key = reportKey(report);
  if (state.cache.text[key]) return state.cache.text[key];

  try {
    const cached = await idbGet('texts', key);
    if (cached) {
      state.cache.text[key] = cached;
      return cached;
    }
  } catch {}

  // Gazettes always uses bundled text-shards. Composite key in the
  // shards is `central|<file_id>` (build_gazettes.py prefixes with
  // category, not corpus, because future state-gazette categories
  // could share the same shard set conceptually).
  const fid = fileId(report);
  const compositeKey = `central|${fid}`;
  try {
    const text = await loadTextFromShards('gazettes', compositeKey, _deps.config.dataBaseUrl);
    if (text !== null) {
      state.cache.text[key] = text;
      idbPut('texts', key, text).catch(() => {});
      _deps.disk?.write?.('gazettes', `text/${fid}.txt`, text).catch(() => {});
      return text;
    }
  } catch (e) {
    console.warn('gazettes: text-shard fetch failed', e);
  }

  // Offline fallback: disk-saved copy from a previous online session.
  try {
    const fromDisk = await _deps.disk?.read?.('gazettes', `text/${fid}.txt`);
    if (fromDisk) {
      state.cache.text[key] = fromDisk;
      idbPut('texts', key, fromDisk).catch(() => {});
      return fromDisk;
    }
  } catch {}
  return null;
}

// ── Filtering / list rendering ──────────────────────────────────────────────

function getAllReports() {
  return (Array.isArray(state.data.reports) ? state.data.reports : []).map(r => ({
    ...r,
    _date: reportDate(r),
    _year: reportYear(r),
    _langInfo: langInfo(r),
  }));
}

function applyFilters() {
  const all = getAllReports();
  const f = state.filters;
  const rawQ = f.search.trim();
  const parsedQ = rawQ ? parseQuery(rawQ) : null;
  const bundle = state.searchBundle;

  const tokenIndexSets = parsedQ
    ? parsedQ.tokens.map(t => expandTokenToDocs(state.searchIndex, t))
    : [];

  const filtered = all.filter(r => {
    if (f.category && r.category !== f.category) return false;
    if (f.ministry && r.ministry !== f.ministry) return false;
    if (f.year && String(r._year || '') !== f.year) return false;
    if (f.language && languageKey(r) !== f.language) return false;
    if (!parsedQ) return true;

    const key = reportKey(r);
    // Subject is the closest analogue to "title". Plus ministry + department.
    const subjectLower  = (r.subject || '').toLowerCase();
    const ministryLower = (r.ministry || '').toLowerCase();
    const deptLower     = (r.department || '').toLowerCase();
    const bundleEntry = bundle ? bundle.map.get(key) : null;
    const headLower   = bundleEntry ? bundleEntry.head.toLowerCase() : '';
    const cached      = state.cache.text[key];
    const cachedLower = cached ? cached.toLowerCase() : '';

    function tokenHits(tok, indexSet) {
      if (indexSet && indexSet.has(key)) return true;
      if (subjectLower.includes(tok)) return true;
      if (ministryLower.includes(tok)) return true;
      if (deptLower.includes(tok)) return true;
      if (headLower && headLower.includes(tok)) return true;
      if (cachedLower && cachedLower.includes(tok)) return true;
      return false;
    }
    function phraseHits(phrase) {
      if (subjectLower.includes(phrase)) return true;
      if (ministryLower.includes(phrase)) return true;
      if (deptLower.includes(phrase)) return true;
      if (headLower && headLower.includes(phrase)) return true;
      if (cachedLower && cachedLower.includes(phrase)) return true;
      return false;
    }

    if (state.matchAny) {
      const hit = parsedQ.tokens.some((t, i) => tokenHits(t, tokenIndexSets[i]))
              ||  parsedQ.phrases.some(phraseHits);
      if (!hit) return false;
    } else {
      if (!parsedQ.tokens.every((t, i) => tokenHits(t, tokenIndexSets[i]))) return false;
      if (!parsedQ.phrases.every(phraseHits)) return false;
    }
    return true;
  });

  filtered.sort((a, b) => {
    switch (f.sort) {
      case 'date_asc':  return (a._date || 0) - (b._date || 0);
      case 'ugid_desc': return parseInt(b.ugid || '0', 10) - parseInt(a.ugid || '0', 10);
      case 'ugid_asc':  return parseInt(a.ugid || '0', 10) - parseInt(b.ugid || '0', 10);
      case 'date_desc':
      default:          return (b._date || 0) - (a._date || 0);
    }
  });

  state.filtered = filtered;
  renderList();
  renderResultsLine();
}

function renderResultsLine() {
  const el = document.getElementById('resultsLine');
  if (!el) return;
  const total = getAllReports().length;
  const shown = state.filtered.length;
  const meta = state.data.meta;
  let metaLine = '';
  if (meta?.generated_at) {
    metaLine = `Mirror updated <b>${escapeHtml(formatLocalTimestamp(meta.generated_at))}</b>`;
  }

  let indexLine = '';
  const bundle = state.searchBundle;
  const idx = state.searchIndex;
  const mirrorWithText = Object.keys(state.data.manifest?.texts || {}).length;
  const loading = state.bundleLoading || state.indexLoading;
  if (loading) {
    const what = state.bundleLoading && state.indexLoading
      ? 'bundle + index'
      : state.bundleLoading ? 'bundle' : 'index';
    indexLine = `<span class="indexing">Loading search ${what}…</span>`;
  } else if (state.deepSearch && state.bundleLoaded && bundle) {
    const tooltip = idx
      ? `Search hits titles + first ${bundle.head_chars} chars of every report, plus body-token presence across the corpus.`
      : `Search hits titles + first ${bundle.head_chars} chars of every report.`;
    indexLine = `<span title="${escapeHtml(tooltip)}">Full-text search · ${bundle.total} reports</span>`
              + ` · <label class="match-any-toggle"><input type="checkbox" id="matchAnyToggle"${state.matchAny ? ' checked' : ''}>match any</label>`;
  } else if (mirrorWithText === 0) {
    indexLine = '';
  } else {
    indexLine = `<span>Title search only · <b>${mirrorWithText}</b> reports with text · <a href="#" id="enableDeepLink" style="color:var(--accent)">enable deep search</a></span>`;
  }
  el.innerHTML = (shown > 0 && shown < total)
    ? `<div class="rl-primary">Showing <b>${shown}</b> of <b>${total}</b></div>`
    : '';

  const enableLink = document.getElementById('enableDeepLink');
  if (enableLink) {
    enableLink.addEventListener('click', (e) => {
      e.preventDefault();
      state.deepSearch = true;
      const s = loadSettings();
      s.gazettes_deepSearch = true;
      saveSettings(s);
      renderResultsLine();
      Promise.all([loadSearchBundle(), loadSearchIndex()]).then(() => {
        renderResultsLine();
        if (state.filters.search) applyFilters();
      });
    });
  }
  const matchAnyToggle = document.getElementById('matchAnyToggle');
  if (matchAnyToggle) {
    matchAnyToggle.addEventListener('change', (e) => {
      state.matchAny = !!e.target.checked;
      const s = loadSettings();
      s.gazettes_matchAny = state.matchAny;
      saveSettings(s);
      applyFilters();
    });
  }

  const sumBtn = document.getElementById('exportSummariesBtn');
  if (sumBtn) {
    const n = Object.keys(state.cache.summaries || {}).length;
    sumBtn.disabled = n === 0;
    sumBtn.textContent = n ? `⬇ Summaries (MD, ${n})` : '⬇ Summaries (MD)';
  }
}

// Bundled-records signal: a record has text if its composite id is in
// the manifest's texts map (build_gazettes derive populates this from
// the texts-meta.json record_to_shard).
function hasExtractedText(r) {
  return !!state.data.manifest?.texts?.[`central|${fileId(r)}`];
}
function hasSummary(r) {
  return !!state.cache.summaries[reportKey(r)];
}

function renderList() {
  const list = document.getElementById('reportsList');
  if (!list) return;
  if (!state.filtered.length) {
    list.innerHTML = `<div class="empty-state"><h3>No notifications match</h3><p>Try clearing filters.</p></div>`;
    return;
  }
  const slice = state.filtered.slice(0, 200);
  const rawQ = state.filters.search.trim();
  const parsedQ = rawQ ? parseQuery(rawQ) : null;
  list.innerHTML = slice.map(r => {
    const subjectHTML = parsedQ ? highlightMatches(r.subject || '(untitled)', parsedQ)
                                : escapeHtml(r.subject || '(untitled)');
    const catShort  = CATEGORY_SHORT[r.category] || '?';
    const catPill   = CATEGORY_PILL[r.category]  || 'L';
    const li        = r._langInfo;
    const subline   = [r.ministry, r.department].filter(Boolean).join(' · ')
                    + (r.gazette_id ? ` · ${r.gazette_id}` : '');
    const dateStr   = formatDate(r._date);
    return `
      <div class="report-row" data-key="${escapeHtml(reportKey(r))}">
        <div>
          <div class="report-title">${subjectHTML}</div>
          <div class="report-meta">${escapeHtml(subline) || '—'}</div>
        </div>
        <span class="house-pill house-${catPill}" title="${escapeHtml(categoryLabel(r))} Gazette">${escapeHtml(catShort)}</span>
        <span class="cat-badge cat-${li.class}" title="${escapeHtml(li.label)}">${escapeHtml(li.short)}</span>
        <span class="report-meta">${escapeHtml(dateStr)}</span>
        <span class="text-status ${hasSummary(r) ? 'summary' : (hasExtractedText(r) ? 'ok' : '')}">
          ${hasSummary(r) ? 'summary' : (hasExtractedText(r) ? 'text' : 'metadata')}
        </span>
      </div>
    `;
  }).join('');
  if (state.filtered.length > 200) {
    list.insertAdjacentHTML('beforeend',
      `<div class="empty-state" style="padding:18px"><p>Showing first 200 of ${state.filtered.length} matches — refine filters to narrow further.</p></div>`);
  }
}

