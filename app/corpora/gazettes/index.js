function populateFilters() {
  const reports = Array.isArray(state.data.reports) ? state.data.reports : [];
  // Compute distinct values per filter dimension from the loaded data.
  const categorySet = new Set();
  const ministrySet = new Set();
  const langSet     = new Set();
  const yearSet     = new Set();
  for (const r of reports) {
    if (r.category) categorySet.add(r.category);
    if (r.ministry) ministrySet.add(r.ministry);
    const lk = languageKey(r);
    if (lk && lk !== '?') langSet.add(lk);
    const y = reportYear(r);
    if (y) yearSet.add(y);
  }

  const catSel = document.getElementById('filterCategory');
  if (catSel) {
    catSel.innerHTML = '<option value="">All categories</option>' +
      [...categorySet].sort().map(v =>
        `<option value="${escapeHtml(v)}">${escapeHtml(v === 'extraordinary' ? 'Extraordinary' : v === 'weekly' ? 'Weekly' : v)}</option>`).join('');
  }
  const minSel = document.getElementById('filterMinistry');
  if (minSel) {
    minSel.innerHTML = '<option value="">All ministries</option>' +
      [...ministrySet].sort().map(v => `<option value="${escapeHtml(v)}">${escapeHtml(v)}</option>`).join('');
  }
  const langSel = document.getElementById('filterLanguage');
  if (langSel) {
    langSel.innerHTML = '<option value="">All languages</option>' +
      [...langSet].sort().map(v => {
        const info = LANG_INFO[v] || DEFAULT_LANG;
        return `<option value="${escapeHtml(v)}">${escapeHtml(info.label)}</option>`;
      }).join('');
  }
  const yearSel = document.getElementById('filterYear');
  if (yearSel) {
    yearSel.innerHTML = '<option value="">All years</option>' +
      [...yearSet].sort((a, b) => b - a).map(v => `<option value="${v}">${v}</option>`).join('');
  }

  // Re-apply persisted state values so corpus-switch round-trips show the
  // active selection in the dropdown UI (not just in state.filters).
  if (catSel)   catSel.value   = state.filters.category || '';
  if (minSel)   minSel.value   = state.filters.ministry || '';
  if (langSel)  langSel.value  = state.filters.language || '';
  if (yearSel)  yearSel.value  = state.filters.year     || '';
}

