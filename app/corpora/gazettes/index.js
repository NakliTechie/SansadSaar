async function fetchStatus() {
  const meta = state.data.meta;
  const withText = Object.keys(state.data.manifest?.texts || {}).length;
  const items = (Array.isArray(state.data.reports) ? state.data.reports.length : 0);
  if (!meta) return { lastUpdate: null, items, withText, error: null };
  return {
    lastUpdate: meta.generated_at || null,
    items: (meta.totals && meta.totals.central) || items,
    withText,
    error: null,
  };
}

// ── JS API surface ─────────────────────────────────────────────────────────

const api = {
  list({ category, ministry, year, language } = {}) {
    return getAllReports().filter(r => {
      if (category && r.category !== category) return false;
      if (ministry && r.ministry !== ministry) return false;
      if (year != null && String(reportYear(r)) !== String(year)) return false;
      if (language && languageKey(r) !== language) return false;
      return true;
    });
  },
  get(key) {
    return getAllReports().find(r => reportKey(r) === key) || null;
  },
  async search(query, opts = {}) {
    if (opts.deep && (!state.bundleLoaded || !state.indexLoaded)) {
      state.deepSearch = true;
      await Promise.all([loadSearchBundle(), loadSearchIndex()]);
    }
    const parsedQ = parseQuery(query);
    if (!parsedQ.tokens.length && !parsedQ.phrases.length) return getAllReports();
    const bundle = state.searchBundle;
    const tokenIndexSets = parsedQ.tokens.map(t => expandTokenToDocs(state.searchIndex, t));
    const anyMode = !!opts.any;
    return getAllReports().filter(r => {
      const key = reportKey(r);
      const subjectLower  = (r.subject || '').toLowerCase();
      const ministryLower = (r.ministry || '').toLowerCase();
      const bundleEntry = bundle ? bundle.map.get(key) : null;
      const headLower   = bundleEntry ? bundleEntry.head.toLowerCase() : '';
      const cached      = state.cache.text[key];
      const cachedLower = cached ? cached.toLowerCase() : '';
      const tokHit = (t, idxSet) => (idxSet && idxSet.has(key))
        || subjectLower.includes(t)
        || ministryLower.includes(t)
        || (headLower && headLower.includes(t))
        || (cachedLower && cachedLower.includes(t));
      const phHit = p => subjectLower.includes(p)
        || ministryLower.includes(p)
        || (headLower && headLower.includes(p))
        || (cachedLower && cachedLower.includes(p));
      if (anyMode) {
        return parsedQ.tokens.some((t, i) => tokHit(t, tokenIndexSets[i]))
            || parsedQ.phrases.some(phHit);
      }
      return parsedQ.tokens.every((t, i) => tokHit(t, tokenIndexSets[i]))
          && parsedQ.phrases.every(phHit);
    });
  },
  open(key) {
    const r = api.get(key);
    if (!r) return false;
    openReportByKey(reportKey(r));
    return true;
  },
  /**
   * Cross-corpus search adapter — calls api.search and pre-formats hits
   * into the shape the global-search modal expects:
   *   { key, title, subtitle, meta? }
   */
  async searchForGlobal(query, { deep = true, limit = 10 } = {}) {
    const items = await api.search(query, { deep });
    const subs = (r) => {
      const parts = [];
      parts.push(CATEGORY_SHORT[r.category] || '?');
      if (r.ministry) parts.push(r.ministry);
      if (r.issue_date) parts.push(r.issue_date);
      if (r.ugid) parts.push(`UGID ${r.ugid}`);
      return parts.join(' · ');
    };
    return items.slice(0, limit).map(r => ({
      key:      reportKey(r),
      title:    r.subject || '(untitled)',
      subtitle: subs(r),
    }));
  },
  categories: () => Object.keys(CATEGORY_SHORT),
  languages:  () => Object.keys(LANG_INFO),
};

