//
// Phase A: Lok Sabha only. The architecture (house-keyed reports,
// house-stratified text/, generic house filter in the UI) is designed
// so Rajya Sabha lands additively when phase B ships.
//
// Source: sansad.in's api_ls/debate/* endpoints. Daily-mirrored via
// parliamentwatch-data's build_debates.py + debates.yml.
//
