// ── Helpers ─────────────────────────────────────────────────────────────────

function reportKey(r) {
  if (r.house === 'ls') return `debates|ls|${r.lok_sabha}|${r.session}|${r.db_slno}`;
  // RS uses ISO date (yyyy-mm-dd) for stable sortability.
  if (r.house === 'rs') return `debates|rs|${r.session}|${r.date_iso || r.date}`;
  return `debates|?|${r.lok_sabha || '?'}|${r.session || '?'}|${r.db_slno || r.date || '?'}`;
}

// LS records have one file_id. RS records have multiple (one per version
// in file_versions). `fileId(r, version='floor')` returns the per-version
// id for RS; for LS the version arg is ignored.
function fileId(r, version = 'floor') {
  if (r.house === 'ls') return `LS${r.lok_sabha}_S${r.session}_${r.db_slno}`;
  if (r.house === 'rs') {
    const iso = r.date_iso || (r.date || '').split('/').reverse().join('-');
    return `RS${r.session}_${iso}_${version}`;
  }
  return '';
}

// RS-only: which versions are actually extracted and available?
function rsAvailableVersions(r) {
  if (r.house !== 'rs') return [];
  const byVer = state.data.manifest?.texts?.rs?.[`RS${r.session}_${r.date_iso || (r.date || '').split('/').reverse().join('-')}`] || {};
  // Order: floor → english → part1 (canonical, then EN translation, then Q&A subset)
  return ['floor', 'english', 'part1'].filter(v => byVer[v]);
}

function parseDate(s) {
  if (!s) return null;
  // sansad.in dates are DD/MM/YYYY
  const m = String(s).match(/^(\d{1,2})\/(\d{1,2})\/(\d{4})$/);
  if (m) {
    const d = new Date(`${m[3]}-${m[2]}-${m[1]}`);
    if (!isNaN(d)) return d;
  }
  const d2 = new Date(s);
  if (!isNaN(d2)) return d2;
  return null;
}

function reportDate(r) {
  if (r.house === 'ls') return parseDate(r.debate_date);
  if (r.house === 'rs') return parseDate(r.date);
  return null;
}

function reportYear(r) {
  const d = reportDate(r);
  return d ? d.getFullYear() : null;
}

function memberLine(r) {
  if (r.house !== 'ls') return '';
  const ms = r.members || [];
  if (!ms.length) return '';
  if (ms.length === 1) return ms[0].mp_name || '';
  if (ms.length === 2) return `${ms[0].mp_name}, ${ms[1].mp_name}`;
  return `${ms[0].mp_name} + ${ms.length - 1} others`;
}

// ── Data fetching ───────────────────────────────────────────────────────────

async function fetchData(forceRefresh = false) {
  const splash    = document.getElementById('splash');
  const dataUrl   = _deps.config.dataBaseUrl;

  if (!forceRefresh) {
    try {
      const [reportsBlob, manifestBlob, metaBlob] = await Promise.all([
        idbGet('blobs', 'debates-reports.json'),
        idbGet('blobs', 'debates-manifest.json'),
        idbGet('blobs', 'debates-meta.json'),
      ]);
      if (reportsBlob && manifestBlob) {
        state.data.reports  = reportsBlob;
        state.data.manifest = manifestBlob;
        state.data.meta     = metaBlob || null;
      }
    } catch (e) { console.warn('debates IDB cache miss', e); }
  }

  const haveCache = Object.values(state.data.reports).some(v => Array.isArray(v) && v.length);
  const bucket = Math.floor(Date.now() / 300000);
  const v = forceRefresh ? `?v=${Date.now()}` : `?v=${bucket}`;
  const fetchOpts = { cache: 'no-cache' };

  try {
    const [reports, manifest, meta] = await Promise.all([
      fetch(dataUrl + CORPUS_PREFIX + 'reports.json'  + v, fetchOpts).then(r => r.ok ? r.json() : Promise.reject(`reports.json: ${r.status}`)),
      fetch(dataUrl + CORPUS_PREFIX + 'manifest.json' + v, fetchOpts).then(r => r.ok ? r.json() : { texts: {} }).catch(() => ({ texts: {} })),
      fetch(dataUrl + CORPUS_PREFIX + 'meta.json'     + v, fetchOpts).then(r => r.ok ? r.json() : null).catch(() => null),
    ]);
    state.data.reports  = reports;
    state.data.manifest = manifest;
    state.data.meta     = meta;

    idbPut('blobs', 'debates-reports.json',  reports).catch(() => {});
    idbPut('blobs', 'debates-manifest.json', manifest).catch(() => {});
    if (meta) idbPut('blobs', 'debates-meta.json', meta).catch(() => {});
  } catch (err) {
    console.error('debates fetch failed', err);
    if (!haveCache) {
      _deps.ui.toast('Could not fetch debates data — see console for details');
      return false;
    }
    _deps.ui.toast('Debates offline — using cached data');
  }

  splash?.classList.add('hide');
  return true;
}

// RS records have multi-version text. `version` is 'floor' (default),
// 'english', or 'part1' — ignored for LS. Cache key is per-version so
// switching versions on the same record doesn't smash cache.
async function fetchReportText(report, version = 'floor') {
  const baseKey = reportKey(report);
  // LS: single body. RS: one cache entry per version.
  const cacheKey = report.house === 'rs' ? `${baseKey}#${version}` : baseKey;
  if (state.cache.text[cacheKey]) return state.cache.text[cacheKey];

  try {
    const cached = await idbGet('texts', cacheKey);
    if (cached) {
      state.cache.text[cacheKey] = cached;
      return cached;
    }
  } catch {}

  // Resolve the manifest entry. For LS, manifest.texts.ls[<file_id>] is
  // a flat {size,url}. For RS, manifest.texts.rs[<base_id>] is a dict
  // keyed by version: {floor:{size,url}, english:{...}, part1:{...}}.
  let entry;
  if (report.house === 'ls') {
    entry = state.data.manifest?.texts?.ls?.[fileId(report)];
  } else if (report.house === 'rs') {
    const base = fileId(report, 'floor').replace(/_floor$/, '');
    entry = state.data.manifest?.texts?.rs?.[base]?.[version];
  }
  if (!entry) return null;
  try {
    const res = await fetch(_deps.config.dataBaseUrl + CORPUS_PREFIX + entry.url);
    if (!res.ok) throw new Error(`HTTP ${res.status}`);
    const text = await res.text();
    state.cache.text[cacheKey] = text;
    idbPut('texts', cacheKey, text).catch(() => {});
    _deps.disk?.write?.('debates', entry.url, text).catch(() => {});
    return text;
  } catch (e) {
    try {
      const fromDisk = await _deps.disk?.read?.('debates', entry.url);
      if (fromDisk) {
        state.cache.text[cacheKey] = fromDisk;
        idbPut('texts', cacheKey, fromDisk).catch(() => {});
        return fromDisk;
      }
    } catch {}
    console.warn('debates: failed to fetch text', e);
    return null;
  }
}

// ── Filtering / list rendering ──────────────────────────────────────────────

function getAllReports() {
  const out = [];
  for (const h of Object.keys(HOUSES)) {
    for (const r of (state.data.reports[h] || [])) {
      out.push({ ...r, house: h, _date: reportDate(r), _year: reportYear(r) });
    }
  }
  return out;
}

function applyFilters() {
  const all = getAllReports();
  const f = state.filters;
  const rawQ = f.search.trim();
  const parsedQ = rawQ ? parseQuery(rawQ) : null;
  const bundle = state.searchBundle;

  const tokenIndexSets = parsedQ
    ? parsedQ.tokens.map(t => expandTokenToDocs(state.searchIndex, t))
    : [];

  const filtered = all.filter(r => {
    if (f.house && r.house !== f.house) return false;
    if (f.lok_sabha && (r.house !== 'ls' || String(r.lok_sabha) !== f.lok_sabha)) return false;
    if (f.session && String(r.session) !== f.session) return false;
    if (f.debate_type && (r.house !== 'ls' || String(r.debate_type) !== f.debate_type)) return false;
    if (f.year && String(r._year || '') !== f.year) return false;
    if (!parsedQ) return true;

    const key = reportKey(r);
    const titleLower  = (r.title || '').toLowerCase();
    const bundleEntry = bundle ? bundle.map.get(key) : null;
    const headLower   = bundleEntry ? bundleEntry.head.toLowerCase() : '';
    const cached      = state.cache.text[key];
    const cachedLower = cached ? cached.toLowerCase() : '';

    function tokenHits(tok, indexSet) {
      if (indexSet && indexSet.has(key)) return true;
      if (titleLower.includes(tok)) return true;
      if (headLower && headLower.includes(tok)) return true;
      if (cachedLower && cachedLower.includes(tok)) return true;
      return false;
    }
    function phraseHits(phrase) {
      if (titleLower.includes(phrase)) return true;
      if (headLower && headLower.includes(phrase)) return true;
      if (cachedLower && cachedLower.includes(phrase)) return true;
      return false;
    }

    if (state.matchAny) {
      const hit = parsedQ.tokens.some((t, i) => tokenHits(t, tokenIndexSets[i]))
              ||  parsedQ.phrases.some(phraseHits);
      if (!hit) return false;
    } else {
      if (!parsedQ.tokens.every((t, i) => tokenHits(t, tokenIndexSets[i]))) return false;
      if (!parsedQ.phrases.every(phraseHits)) return false;
    }
    return true;
  });

  filtered.sort((a, b) => {
    switch (f.sort) {
      case 'oldest':  return (a._date || 0) - (b._date || 0);
      case 'house':   return (a.house || '').localeCompare(b.house || '')
                          || ((b._date || 0) - (a._date || 0));
      case 'type':    return (a.debate_type_desc || '').localeCompare(b.debate_type_desc || '')
                          || ((b._date || 0) - (a._date || 0));
      case 'newest':
      default:        return (b._date || 0) - (a._date || 0)
                          || ((b.db_slno || 0) - (a.db_slno || 0));
    }
  });

  state.filtered = filtered;
  renderList();
  renderResultsLine();
}

function renderResultsLine() {
  const el = document.getElementById('resultsLine');
  if (!el) return;
  const total = getAllReports().length;
  const shown = state.filtered.length;
  const meta = state.data.meta;
  let metaLine = '';
  if (meta?.generated_at) {
    metaLine = `Mirror updated <b>${escapeHtml(formatLocalTimestamp(meta.generated_at))}</b>`;
  }

  let indexLine = '';
  const bundle = state.searchBundle;
  const idx = state.searchIndex;
  const mirrorWithText = Object.values(state.data.manifest?.texts || {}).reduce(
    (n, byFid) => n + Object.keys(byFid || {}).length, 0);
  const loading = state.bundleLoading || state.indexLoading;
  if (loading) {
    const what = state.bundleLoading && state.indexLoading
      ? 'bundle + index'
      : state.bundleLoading ? 'bundle' : 'index';
    indexLine = `<span class="indexing">Loading search ${what}…</span>`;
  } else if (state.deepSearch && state.bundleLoaded && bundle) {
    const tooltip = idx
      ? `Search hits titles + first ${bundle.head_chars} chars of every debate, plus body-token presence across the corpus.`
      : `Search hits titles + first ${bundle.head_chars} chars of every debate.`;
    indexLine = `<span title="${escapeHtml(tooltip)}">Full-text search · ${bundle.total} debates</span>`
              + ` · <label class="match-any-toggle"><input type="checkbox" id="matchAnyToggle"${state.matchAny ? ' checked' : ''}>match any</label>`;
  } else if (mirrorWithText === 0) {
    indexLine = '';
  } else {
    indexLine = `<span>Title search only · <b>${mirrorWithText}</b> debates with text · <a href="#" id="enableDeepLink" style="color:var(--accent)">enable deep search</a></span>`;
  }
  el.innerHTML = `<div class="rl-primary">Showing <b>${shown}</b> of <b>${total}</b> parliamentary debates. ${metaLine}</div>${indexLine ? `<div class="rl-secondary">${indexLine}</div>` : ''}`;

  const enableLink = document.getElementById('enableDeepLink');
  if (enableLink) {
    enableLink.addEventListener('click', (e) => {
      e.preventDefault();
      state.deepSearch = true;
      const s = loadSettings();
      s.debates_deepSearch = true;
      saveSettings(s);
      renderResultsLine();
      Promise.all([loadSearchBundle(), loadSearchIndex()]).then(() => {
        renderResultsLine();
        if (state.filters.search) applyFilters();
      });
    });
  }
  const matchAnyToggle = document.getElementById('matchAnyToggle');
  if (matchAnyToggle) {
    matchAnyToggle.addEventListener('change', (e) => {
      state.matchAny = !!e.target.checked;
      const s = loadSettings();
      s.debates_matchAny = state.matchAny;
      saveSettings(s);
      applyFilters();
    });
  }

  const sumBtn = document.getElementById('exportSummariesBtn');
  if (sumBtn) {
    const n = Object.keys(state.cache.summaries || {}).length;
    sumBtn.disabled = n === 0;
    sumBtn.textContent = n ? `⬇ Summaries (MD, ${n})` : '⬇ Summaries (MD)';
  }
}

function hasExtractedText(r) {
  if (r.house === 'ls') {
    return !!state.data.manifest?.texts?.ls?.[fileId(r)];
  }
  if (r.house === 'rs') {
    // Any extracted version counts. The detail panel surfaces all
    // available versions; the user can pick. Floor is preferred order
    // but absence of Floor doesn't disqualify the record.
    return rsAvailableVersions(r).length > 0;
  }
  return false;
}
function hasSummary(r) {
  return !!state.cache.summaries[reportKey(r)];
}

function renderList() {
  const list = document.getElementById('reportsList');
  if (!list) return;
  if (!state.filtered.length) {
    list.innerHTML = `<div class="empty-state"><h3>No debates match</h3><p>Try clearing filters.</p></div>`;
    return;
  }
  const slice = state.filtered.slice(0, 200);
  const rawQ = state.filters.search.trim();
  const parsedQ = rawQ ? parseQuery(rawQ) : null;
  list.innerHTML = slice.map(r => {
    const titleHTML = parsedQ ? highlightMatches(r.title || '(untitled)', parsedQ)
                              : escapeHtml(r.title || '(untitled)');
    const house    = HOUSES[r.house] || { short: '??' };
    const housePill = HOUSE_PILL_CLASS[r.house] || 'L';
    const dateStr  = r.debate_date || r.date || '—';
    const sublineParts = [];
    if (r.house === 'ls') {
      sublineParts.push(`LS${r.lok_sabha} · S${r.session}`);
      const m = memberLine(r);
      if (m) sublineParts.push(m);
      if (r.debate_type_desc) sublineParts.push(r.debate_type_desc);
    } else {
      sublineParts.push(`Session ${r.session}`);
    }
    const subline = sublineParts.map(escapeHtml).join(' · ');
    return `
      <div class="report-row" data-key="${escapeHtml(reportKey(r))}">
        <div>
          <div class="report-title">${titleHTML}</div>
          <div class="report-meta">${subline}</div>
        </div>
        <span class="house-pill house-${housePill}" title="${escapeHtml(house.long || '')}">${escapeHtml(house.short)}</span>
        <span class="report-meta">${escapeHtml(dateStr)}</span>
        <span class="text-status ${hasSummary(r) ? 'summary' : (hasExtractedText(r) ? 'ok' : '')}">
          ${hasSummary(r) ? 'summary' : (hasExtractedText(r) ? 'text' : 'metadata')}
        </span>
      </div>
    `;
  }).join('');
  if (state.filtered.length > 200) {
    list.insertAdjacentHTML('beforeend',
      `<div class="empty-state" style="padding:18px"><p>Showing first 200 of ${state.filtered.length} matches — refine filters to narrow further.</p></div>`);
  }
}

