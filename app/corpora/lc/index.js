// Pill colour rotation by Commission term. Cycles through the four
// cat-badge palette buckets already defined in index.html so the pill
// ribbon reads as a visual grouping at a glance. Buckets:
//   DFG    — green   (1, 5, 9, 13, 17, 21)
//   AT     — navy    (2, 6, 10, 14, 18, 22)
//   SUBJ   — orange  (3, 7, 11, 15, 19)
//   ASSURE — neutral (4, 8, 12, 16, 20)
function commissionPillClass(term) {
  const buckets = ['DFG', 'AT', 'SUBJ', 'ASSURE'];
  return buckets[(term - 1) % buckets.length];
}

