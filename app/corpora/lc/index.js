// ── Activation / lifecycle ──────────────────────────────────────────────────

async function activate(deps, { silent = false } = {}) {
  _deps = deps;

  // Always re-render filter row + handlers on visible activation so a
  // corpus switch back to LC restores the right UI in #filtersContainer.
  // Skip when silent (preload mode — shell prefetches data for cross-
  // corpus search without disturbing the visible DOM that belongs to
  // another corpus).
  if (!silent) {
    renderFilterRow();
    attachHandlers();
  }

  if (!_activated) {
    _activated = true;

    const settings = loadSettings();
    state.deepSearch = !!settings.lc_deepSearch;
    state.matchAny   = !!settings.lc_matchAny;

    const ok = await fetchData();
    if (!ok) return false;

    if (!silent) {
      populateFilters();
      renderHeaderStats();
      applyFilters();
    }

    loadCachedSummaries().then(() => { if (!silent) renderList(); });
    loadCachedChats();
    loadCachedTexts().then((n) => {
      if (n && !silent) {
        renderList();
        renderResultsLine();
      }
      if (state.deepSearch) {
        loadSearchBundle();
        loadSearchIndex();
      }
    });
    // Archive index is small (~50KB at full corpus) and best-effort; fire-
    // and-forget so it doesn't gate the splash. If a user opens a report
    // before this lands, the Archive line just doesn't render yet — they
    // see it on the next open after the index lands.
    fetchArchiveIndex();
  } else if (!silent) {
    populateFilters();
    renderHeaderStats();
    applyFilters();
  }

  return true;
}

