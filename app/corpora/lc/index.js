function populateFilters() {
  const reports = Object.values(state.data.reports);
  const termSet = new Set();
  const yearSet = new Set();
  for (const r of reports) {
    if (r.commission_term != null) termSet.add(r.commission_term);
    if (r.year) yearSet.add(r.year);
  }

  const termSel = document.getElementById('filterCommissionTerm');
  if (termSel) {
    // Sort terms descending (22nd → 1st) so the active Commission is on top.
    termSel.innerHTML = '<option value="">All Commissions</option>' +
      [...termSet].sort((a, b) => b - a).map(t =>
        `<option value="${t}">${escapeHtml(commissionLabel(t))} Commission</option>`).join('');
  }
  const yearSel = document.getElementById('filterYear');
  if (yearSel) {
    yearSel.innerHTML = '<option value="">All years</option>' +
      [...yearSet].sort((a, b) => b - a).map(v => `<option value="${v}">${v}</option>`).join('');
  }

  // Re-apply persisted state values so corpus-switch round-trips show the
  // active selection in the dropdown UI (not just in state.filters).
  if (termSel) termSel.value = state.filters.commission_term || '';
  if (yearSel) yearSel.value = state.filters.year || '';
}

